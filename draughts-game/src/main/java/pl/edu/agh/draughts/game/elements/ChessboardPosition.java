package pl.edu.agh.draughts.game.elements;

public class ChessboardPosition {

	private int row;

	private int column;

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getColumn() {
		return column;
	}

	public void setColumn(int column) {
		this.column = column;
	}

}
